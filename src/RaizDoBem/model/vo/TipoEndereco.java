package RaizDoBem.model.vo;

/**
 * Representa o tipo de endereço, utilizado para identificar se um endereço é residencial ou profissional. Esta enumeração é utilizada para armazenar e manipular informações relacionadas ao tipo de endereço dos beneficiários, colaboradores ou dentistas envolvidos nas atividades da ONG.
 * @author Paulo
 * @since 2026-03
 * RESIDENCIAL - Indica que o endereço é do tipo residencial, utilizado para identificar a residência dos beneficiários, colaboradores ou dentistas.
 * PROFISSIONAL - Indica que o endereço é do tipo profissional, utilizado para identificar o local de trabalho ou atendimento dos beneficiários, colaboradores ou dentistas.
 */
public enum TipoEndereco {
    RESIDENCIAL,
    PROFISSIONAL
}
